#include <unistd.h>

int	ft_atoi(char *str)
{
	int	i;
	int	s;
	int	nb;

	nb = 0;
	i = 0;
	s = 1;
	while (str[i] != '\0' && (str[i] == ' '
			|| str[i] == '\t' || str[i] == '\v'
			|| str[i] == '\r' || str[i] == '\f' || str[i] == '\n'))
	{
		i++;
	}
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			s *= -1;
		i++;
	}
	while (str[i] != '\0' && (str[i] >= '0' && str[i] <= '9'))
	{
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
	return (nb * s);
}
