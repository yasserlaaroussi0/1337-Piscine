int	ft_iterative_factorial(int nb)
{
	int	f;

	f = 1;
	if (nb < 0)
		return (0);
	if (nb == 1 || nb == 0)
		return (1);
	while (nb >= 1)
	{
		f = f * nb;
		nb--;
	}
	return (f);
}
